# String slicing example

text = "PythonProgramming"

# Slicing examples
print("Original String:", text)
print("First 6 characters:", text[:6])          # Python
print("Last 6 characters:", text[-6:])        # ramming
print("Characters from 2 to 8:", text[2:9])    # thonPro
print("Every second character:", text[::2])    # Pto rgamn
print("Reversed string:", text[::-1])          # gnimargorPnohtyP