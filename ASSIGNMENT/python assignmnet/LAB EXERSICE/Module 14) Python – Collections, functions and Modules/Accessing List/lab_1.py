# Creating a list with multiple data types
my_list = [10, "Hello", 3.14, True, [1, 2, 3]]

print("List with multiple data types:", my_list)

print()
print("Now we will accessing elements")

# Accessing elements at different positions
print("First element:", my_list[0])     # integer
print("Second element:", my_list[1])    # string
print("Third element:", my_list[2])     # float
print("Fourth element:", my_list[3])    # boolean
print("Fifth element:", my_list[4])     # sub-list



# Another list example
data = ["Python", 25, 7.9, False, ("A", "B")]

print("List with multiple data type elements:", data)


# Finding length of a list
print("Length of my_list:", len(my_list))
print("Length of data:", len(data))
